#include "philo.h"

long	check_nbr(const char *str)
{
	long	n;
	size_t	i;

	n = 0;
	i = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i ++;
	}
	if (str[i] && n == 0)
		return (-1);
	return (n);
}
